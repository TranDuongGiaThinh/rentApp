class Product {
  int id;
  String pathImage;
  String name;
  String price;
  int bedrooms;
  int bathroom;
  String address;

  Product({
    required this.id,
    required this.pathImage,
    required this.name,
    required this.price,
    required this.bedrooms,
    required this.bathroom,
    required this.address,
  });
}
